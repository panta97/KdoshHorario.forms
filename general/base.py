
class XlsRange:

    def __init__(self, frow, fcol, lrow, lcol):
        self.frow = frow
        self.fcol = fcol
        self.lrow = lrow
        self.lcol = lcol
    
    def frow(self):
        return self.frow
    def fcol(self):
        return self.fcol
    def lrow(self):
        return self.lrow
    def lcol(self):
        return self.lcol